#include <iostream> // подключение библиотеки для ввода-вывода

// Функция сортировки вставками
void insertionSort(int arr[], int size) {
    // Перебираем элементы массива, начиная со второго (индекс 1)
    for (int i = 1; i < size; i++) {
        int key = arr[i]; // текущий элемент, который будем вставлять на правильную позицию
        int j = i - 1;    // индекс предыдущего элемента

        // Пока не достигли начала массива и текущий элемент меньше рассматриваемого
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j]; // сдвигаем элемент вправо
            j--; // переходим к следующему элементу слева
        }
        // Вставляем ключ на найденную позицию
        arr[j + 1] = key;
    }
}

int main() {
    // Объявляем и инициализируем массив
    int arr[] = {9, 5, 1, 4, 3};
    int size = sizeof(arr) / sizeof(arr[0]); // вычисляем размер массива

    // Вывод массива до сортировки
    std::cout << "До сортировки: ";
    for (int i = 0; i < size; i++)
        std::cout << arr[i] << " ";
    std::cout << std::endl;

    // Вызов функции сортировки вставками
    insertionSort(arr, size);

    // Вывод массива после сортировки
    std::cout << "После сортировки: ";
    for (int i = 0; i < size; i++)
        std::cout << arr[i] << " ";
    std::cout << std::endl;

    return 0;
}